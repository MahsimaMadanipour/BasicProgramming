#include <stdio.h>

int main()
{
    int n = 7, m = 12;
    printf("n = %d\n", n);
    printf("m = %d\n", m);

    float f = 6.7;
    printf("f = %f\n", f);

    char ch = 'A';
    printf("ch = %c\n", ch);
    printf("ch = %d\n", ch);

    ch = ch + 5;
    printf("ch = %c\n", ch);
    printf("ch = %d\n", ch);

    ch = '0';
    printf("ch = %c\n", ch);
    printf("ch = %d\n", ch);

    return 0;
}
